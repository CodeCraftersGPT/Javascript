//Basic Template Literal:

const name = "Alice";
const greeting = `Hello, ${name}!`;
console.log(greeting); // Output: "Hello, Alice!"

//Multiline Text:

const message = `
  This is a multiline
  text using template literals.
  It maintains formatting.
`;
console.log(message);
/* Output:
   This is a multiline
   text using template literals.
   It maintains formatting.
*/

//Expression Evaluation:

const price = 10;
const quantity = 5;
const total = `Total cost: $${price * quantity}`;
console.log(total); // Output: "Total cost: $50"







